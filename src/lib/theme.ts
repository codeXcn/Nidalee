// 类型定义
export type ColorThemeName = keyof typeof colorThemes
export type ColorOption = (typeof colors)[number]
export type RadiusOption = (typeof radiusOptions)[number]
export type StyleOption = (typeof styles)[number]

// 主题应用函数
export function applyColorTheme(colorName: ColorThemeName) {
  const theme = colorThemes[colorName]
  if (!theme) return

  // 移除现有的样式元素
  const existingStyle = document.getElementById('theme-variables')
  if (existingStyle) {
    existingStyle.remove()
  }

  // 创建新的样式元素
  const style = document.createElement('style')
  style.id = 'theme-variables'

  // 生成亮色和暗色模式的 CSS
  const lightColors = theme.light
  const darkColors = theme.dark

  let css = ':root {\n'
  Object.entries(lightColors).forEach(([key, value]) => {
    css += `  --${key}: ${value};\n`
  })
  css += '}\n\n'

  css += '.dark {\n'
  Object.entries(darkColors).forEach(([key, value]) => {
    css += `  --${key}: ${value};\n`
  })
  css += '}\n'

  style.textContent = css
  document.head.appendChild(style)

  console.log('[Theme] 应用颜色主题:', colorName)
}
